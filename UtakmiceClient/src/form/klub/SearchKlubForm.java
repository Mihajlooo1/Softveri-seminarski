    private void txtParamKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtParamKeyReleased
        try {
            String param = txtParam.getText();
            List<Klub> list;
            if (param.equals("")) {
                list = ClientController.getInstance().getAllKlub();
            } else {
                list = ClientController.getInstance().searchKlub(param);
            }
            tableModelKlub.setList(list);
        } catch (Exception ex) {
            ex.printStackTrace();
            if (ex instanceof SocketException) {
                JOptionPane.showMessageDialog(this, "Server vam je prekinuo komunikaciju! Gasenje programa...", "Greska", JOptionPane.ERROR_MESSAGE);
                System.exit(0);
            }
            JOptionPane.showMessageDialog(this, ex.getMessage(), "Greska", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_txtParamKeyReleased

