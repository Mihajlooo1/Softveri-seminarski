    private void tblKlubMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblKlubMouseClicked
        try{
            int row = tblKlub.getSelectedRow();
            if (row > -1) {
                Klub klub = tableModelKlub.getSelected(row);

               tableModelStatistikaKluba.setList(klub.getStatistikaKluba());

               List<Igrac> igraci = ClientController.getInstance().getAllIgracFromKlub(klub);
               tableModelIgrac.setList(igraci);
               ispisiOpisStatistike();
            }
        }catch(Exception ex){
            ex.printStackTrace();
            if (ex instanceof SocketException) {
                JOptionPane.showMessageDialog(this, "Server vam je prekinuo komunikaciju! Gasenje programa...", "Greska", JOptionPane.ERROR_MESSAGE);
                System.exit(0);
            }
            JOptionPane.showMessageDialog(this, ex.getMessage(), "Greska", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_tblKlubMouseClicked

