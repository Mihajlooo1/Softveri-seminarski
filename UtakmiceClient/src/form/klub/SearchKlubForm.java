    private void ispisiOpisStatistike(){
        List<StatistikaKluba> list = tableModelStatistikaKluba.getList();
        
        int ukupnoBodova=0;
        int ukupnoDatihGolova = 0;
        int ukupnoPrimljenihGolova = 0;
        
        for(StatistikaKluba sk: list){
            ukupnoBodova+= sk.getBrojBodova();
            ukupnoDatihGolova+= sk.getBrojDatihGolova();
            ukupnoPrimljenihGolova+= sk.getBrojPrimljenihGolova();
        }
        
        lblOpis.setText("Ukupno bodova: "+ ukupnoBodova+"; ukupno datih golova: "+ukupnoDatihGolova+"; ukupno primljenih golova: "+ ukupnoPrimljenihGolova);
    }
