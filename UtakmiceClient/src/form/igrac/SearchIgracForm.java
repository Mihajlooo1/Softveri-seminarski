    private void btnIzbrisiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIzbrisiActionPerformed
        try {
            int row = tblIgraci.getSelectedRow();
            if (row > -1) {
                Igrac igrac = tableModel.getSelected(row);
                int option = JOptionPane.showConfirmDialog(rootPane, "Da li ste sigurni da zelite da igraca: "+ igrac+"?");
                if(option == JOptionPane.YES_OPTION){
                    ClientController.getInstance().deleteIgrac(igrac);
                    JOptionPane.showMessageDialog(rootPane, "Uspesno ste obrisali igraca");
                    this.dispose();
                }
            }
        } catch (Exception ex) {
            if (ex instanceof SocketException) {
                JOptionPane.showMessageDialog(this, "Server je prekinuo komunikaciju", "Greska", JOptionPane.ERROR_MESSAGE);
                System.exit(0);
            }
            JOptionPane.showMessageDialog(this, ex.getMessage(), "Greska", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnIzbrisiActionPerformed


