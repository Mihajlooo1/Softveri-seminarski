    private void txtPretragaKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtPretragaKeyReleased
        try {
            String param = txtPretraga.getText().trim();
            List<Igrac> list;
            if (param.isEmpty()) {
                list = ClientController.getInstance().getAllIgrac();
            } else {
                list = ClientController.getInstance().searchIgrac(param);
            }
            tableModel.setList(list);
        } catch (Exception ex) {
            if (ex instanceof SocketException) {
                JOptionPane.showMessageDialog(this, "Server je prekinuo komunikaciju", "Greska", JOptionPane.ERROR_MESSAGE);
                System.exit(0);
            }
            JOptionPane.showMessageDialog(this, ex.getMessage(), "Greska", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_txtPretragaKeyReleased

